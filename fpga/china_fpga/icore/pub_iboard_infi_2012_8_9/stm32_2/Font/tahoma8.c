/*-------------------------------------------------------
   NAME       : --
   ENTRY  TYPE: void
   RETURN TYPE: void
   PURPOSE    : ---
   VERSION    : 0.0.0
   DESCRIPTION:
   CREATE DATE: ---
   LOGS       :
   ---------------------------------------------------------*/
static int
get(FONT_MATRIX_T *f)
{
	f->direction = DIRECTION_V;
	f->height = 16;
	f->width = tahoma8_rc.offset[f->character[0] - ' ' + 1] - tahoma8_rc.offset[f->character[0] - ' '];
	f->dat = tahoma8_rc.dat + tahoma8_rc.offset[f->character[0] - ' '] * 2;

	return 0;
}

